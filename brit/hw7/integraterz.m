function drz = integraterz(t,y)
% Defined Constants
Hze = 0.3;
J2 = 0.00108263;


rho = sqrt(y(1)^2 + y(3)^2);
drz = [y(2); ((Hze/(y(1)^3)) - (y(1)/rho^3) ...
    - (3*J2/2*y(1)/rho^5) ...
    + (15*J2/2*y(1)*y(3)^2 / rho^7));...
    y(4); ((-y(3)/rho^3) - (3*J2/2*y(3)/rho^5) ...
    + (3*J2/2*(3*y(3)^2 - 2 * y(1)^2)*y(3)/rho^7)); ...
    (sqrt(Hze)/y(1)^2)];
end